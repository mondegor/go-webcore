package mridempotency

type (
	// Responser - определяет интерфейс для возврата сохранённого ответа
	// из хранилища идемпотентности.
	//
	// Реализации этого интерфейса используются для сериализации и десериализации
	// HTTP-ответов, сохранённых при предыдущих запросах с тем же ключом.
	Responser interface {
		// Content - возвращает тело (содержимое) HTTP-ответа.
		// Может возвращать nil, если ответ пустой.
		Content() []byte

		// StatusCode - возвращает HTTP-код ответа (например: 200, 404, 500).
		StatusCode() int
	}
)
